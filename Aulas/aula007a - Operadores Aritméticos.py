'''Nessa aula, vamos aprender quais são os operadores aritméticos do Python e também sua
ordem de precedência dentro de expressões matemáticas. Veja como funcionam os operadores de adição,
subtração, multiplicação, divisão, exponenciação e quociente na linguagem Python.'''

nome = input("Qual é o seu nome?")
print("prazer em te conhecer {:20}!".format(nome))
print("prazer em te conhecer {:<20}!".format(nome))
print("prazer em te conhecer {:>20}!".format(nome))
print("prazer em te conhecer {:^20}!".format(nome))
print("prazer em te conhecer {:=^20}!".format(nome))

